import cv2
import matplotlib.pyplot as plt

# Load the image
image_path = "/home/ankit/Documents/STUDY/RBE594/HW1/Calibration_Imgs/IMG_20170209_042624.jpg"  # Replace with your image path
image = cv2.imread(image_path)

# Coordinates of the point (x, y)
point = (723,1646)  # Replace with your point's coordinates
print(image.shape)
# Plot the point
# Note: OpenCV uses BGR format, but Matplotlib expects RGB format
